
num=input("숫자 입력 : ")

if 98 <= int(num) <=100:
  print("A+")
elif 94 <= int(num) <=97:
  print("A")
elif 90 <= int(num) <=93:
  print("A-")
  
elif 88 <= int(num) <90:
  print("B+")
elif 84 <= int(num) <=87:
  print("B")
elif 80 <= int(num) <=83:
  print("B-")
  
elif 78 <= int(num) <80:
  print("C+")
elif 74 <= int(num) <=77:
  print("C")
elif 70 <= int(num) <=73:
  print("C-")

elif 68 <= int(num) <70:
  print("D+")
elif 64 <= int(num) <=67:
  print("D")
elif 60 <= int(num) <=63:
  print("D-")
else:
  print("F")




